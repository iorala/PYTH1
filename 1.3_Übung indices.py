# Übung String indices  - p 18
# Gegeben ist folgender String:
# 
# "Hallo Welt! Dies ist mein erstes Python-Programm."
satz =  "Hallo Welt! Dies ist mein erstes Python-Programm."
# 
print("Geben Sie nur den ersten Satz aus")
print(satz[0:11])
print("Geben Sie nur den zweiten Satz aus.")
print(satz[12:])
print("Geben Sie nur den ersten Satz rückwärts aus.")
print(satz[-38::-1])
print("Geben Sie nur den zweiten Satz rückwärts aus.")
print(satz[:-39:-1])
print("Geben Sie nur jeden 4. Buchstaben des zweiten Satzes aus.")
print(satz[12::4])

